/**
 * Generate `src/data/datasets/<id>/kheldian-form-variants.ts` from
 * `exported_powers/<id>/kheldian_pets/{bright_nova,white_dwarf,dark_nova,
 * black_dwarf}/*.json`.
 *
 * These JSONs contain the actual effect data for Kheldian form-variant
 * powers. Some variants (Bright_Nova_Bolt) are duplicated under the
 * human powerset folder; others (White_Dwarf_Bolt, Black_Dwarf_Bolt,
 * Glinting_Eye Dwarf variants) live ONLY under Kheldian_Pets and don't
 * appear in any human powerset. We need a runtime lookup keyed by
 * internalName so the InfoPanel form-redirect resolver can find them.
 *
 * Usage:
 *   node scripts/generate-kheldian-variants.cjs --dataset rebirth
 *   node scripts/generate-kheldian-variants.cjs --dataset homecoming
 */

const fs = require('fs');
const path = require('path');
const { parseDatasetArg, datasetPath } = require('./_dataset-paths.cjs');
const { extractEffects, extractDamage, normalizeIconPath, collectAllTemplates, EFFECT_AREA_MAP } = require('./convert-powerset.cjs');

const datasetId = parseDatasetArg();
const RAW_DATA_BASE = path.join(__dirname, '../exported_powers');
const RAW_DATA_PATH = (datasetId === 'homecoming' && !fs.existsSync(path.join(RAW_DATA_BASE, datasetId)))
  ? RAW_DATA_BASE
  : path.join(RAW_DATA_BASE, datasetId);
const KHELDIAN_PETS_PATH = path.join(RAW_DATA_PATH, 'kheldian_pets');

const FORM_FOLDERS = ['bright_nova', 'white_dwarf', 'dark_nova', 'black_dwarf'];

const OUTPUT_PATH = datasetPath(datasetId, 'kheldian-form-variants.ts');

function loadVariants() {
  const variants = {};
  if (!fs.existsSync(KHELDIAN_PETS_PATH)) {
    console.warn(`No kheldian_pets data at ${KHELDIAN_PETS_PATH}; emitting empty map.`);
    return variants;
  }
  for (const form of FORM_FOLDERS) {
    const formDir = path.join(KHELDIAN_PETS_PATH, form);
    if (!fs.existsSync(formDir)) continue;
    for (const file of fs.readdirSync(formDir)) {
      if (!file.endsWith('.json') || file === 'index.json') continue;
      const raw = JSON.parse(fs.readFileSync(path.join(formDir, file), 'utf8'));
      const internalName = raw.name;
      if (!internalName) continue;

      // Build a Power-shaped object with just the fields the InfoPanel's
      // form-redirect overlay reads (stats, damage, effects, shortHelp,
      // description, effectArea, targetType, powerType).
      const stats = {};
      if (raw.accuracy != null) stats.accuracy = raw.accuracy;
      if (raw.range != null && raw.range > 0) stats.range = raw.range;
      if (raw.recharge_time != null && raw.recharge_time > 0) stats.recharge = raw.recharge_time;
      if (raw.endurance_cost != null) stats.endurance = raw.endurance_cost;
      if (raw.activation_time != null && raw.activation_time > 0) stats.castTime = raw.activation_time;
      if (raw.activate_period != null && raw.activate_period > 0) stats.activatePeriod = raw.activate_period;
      if (raw.radius != null && raw.radius > 0) stats.radius = raw.radius;
      if (raw.arc != null && raw.arc > 0) stats.arc = raw.arc;
      if (raw.max_targets_hit != null && raw.max_targets_hit > 0) stats.maxTargets = raw.max_targets_hit;

      const templates = collectAllTemplates(raw.effects || []);
      const damage = extractDamage(templates);
      const effects = extractEffects(templates);

      variants[internalName] = {
        name: raw.display_name && !raw.display_name.startsWith('P') ? raw.display_name
            : (internalName.replace(/_/g, ' ')),
        internalName,
        powerType: raw.type || 'Click',
        targetType: raw.target_type || 'Foe',
        effectArea: EFFECT_AREA_MAP[raw.effect_area] ?? raw.effect_area ?? 'SingleTarget',
        shortHelp: raw.display_short_help || '',
        description: raw.display_help || '',
        icon: normalizeIconPath(raw.icon || ''),
        stats,
        ...(damage ? { damage } : {}),
        ...(effects && Object.keys(effects).length > 0 ? { effects } : {}),
      };
    }
  }
  return variants;
}

function emit(variants) {
  const header = `/**
 * Kheldian form-variant power lookup (Rebirth).
 *
 * AUTO-GENERATED by \`scripts/generate-kheldian-variants.cjs\`. Do not
 * hand-edit. Re-run when the bin export changes.
 *
 * Source: \`exported_powers/${datasetId}/kheldian_pets/{form}/*.json\`.
 *
 * These are the activation-time redirect targets for human-form
 * Kheldian attacks. Looked up by internalName in the InfoPanel's
 * form-redirect overlay.
 */

import type { Power } from '@/types';

export const KHELDIAN_FORM_VARIANT_POWERS: Record<string, Partial<Power>> = ${JSON.stringify(variants, null, 2)};
`;
  fs.mkdirSync(path.dirname(OUTPUT_PATH), { recursive: true });
  fs.writeFileSync(OUTPUT_PATH, header);
  console.log(`Wrote ${Object.keys(variants).length} variants to ${OUTPUT_PATH}`);
}

const variants = loadVariants();
emit(variants);
